/* ── Window Ring ── */
function startWindowRing(durationSec = 90) {
    if (windowArcInterval) clearInterval(windowArcInterval);
    const arc = document.getElementById('windowArc');
    const fill = document.getElementById('wtFill');
    const fill2 = document.getElementById('wtFill2');
    const wt = document.getElementById('wtLabel');
    const total = windowArcTotal;
    let remaining = durationSec;
    function update() {
        const pct = remaining / durationSec;
        const offset = total * (1 - pct);
        if (arc) arc.style.strokeDashoffset = offset;
        if (fill) fill.style.width = `${pct * 100}%`;
        if (fill2) fill2.style.width = `${pct * 100}%`;
        if (wt) wt.innerText = `Window · ${remaining}s`;
        if (remaining <= 0) clearInterval(windowArcInterval);
        remaining--;
    }
    update();
    windowArcInterval = setInterval(update, 1000);
}

