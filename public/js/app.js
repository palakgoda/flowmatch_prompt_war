function startCountdown(secs) {
    if (countdownInterval) clearInterval(countdownInterval);
    countdownSec = secs; const totalDash = 295.31;
    updateRing(secs, totalDash);
    countdownInterval = setInterval(() => { countdownSec--; if (countdownSec < 0) { clearInterval(countdownInterval); return; } updateRing(countdownSec, totalDash); }, 1000);
}
function updateRing(remaining, total) {
    const pct = remaining / total, offset = total * (1 - pct);
    const arc = document.getElementById('ringArc'), timer = document.getElementById('cdTimer');
    if (!arc || !timer) return;
    arc.style.strokeDashoffset = offset;
    const m = Math.floor(remaining / 60).toString().padStart(2, '0');
    const s = (remaining % 60).toString().padStart(2, '0');
    timer.innerText = `${m}:${s}`;
}

