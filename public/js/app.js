async function startQRScanner() {
    const wrap = document.getElementById('qrWrap'), video = document.getElementById('qrVideo'), status = document.getElementById('qrStatus');
    wrap.classList.add('visible'); qrScanned = false; status.className = ''; status.innerText = 'Point camera at QR code...';
    try { qrStream = await navigator.mediaDevices.getUserMedia({ video: { facingMode: 'environment' } }); video.srcObject = qrStream; video.play(); video.onloadedmetadata = () => scanLoop(); }
    catch { status.className = 'error'; status.innerText = '⚠ Camera access denied. Use manual entry.'; }
}
function stopQRScanner() {
    if (qrAnimFrame) { cancelAnimationFrame(qrAnimFrame); qrAnimFrame = null; }
    if (qrStream) { qrStream.getTracks().forEach(t => t.stop()); qrStream = null; }
    document.getElementById('qrWrap').classList.remove('visible');
}
function scanLoop() {
    if (qrScanned) return;
    const video = document.getElementById('qrVideo'), canvas = document.getElementById('qrCanvas'), ctx = canvas.getContext('2d');
    if (video.readyState === video.HAVE_ENOUGH_DATA) {
        canvas.width = video.videoWidth; canvas.height = video.videoHeight;
        ctx.drawImage(video, 0, 0, canvas.width, canvas.height);
        const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
        const code = jsQR(imageData.data, imageData.width, imageData.height, { inversionAttempts: 'dontInvert' });
        if (code) { qrScanned = true; onQRDetected(code.data); return; }
    }
    qrAnimFrame = requestAnimationFrame(scanLoop);
}
function onQRDetected(rawData) {
    const status = document.getElementById('qrStatus'), ticket = rawData.trim().toUpperCase();
    status.className = 'success'; status.innerText = `✓ Detected: ${ticket}`;
    stopQRScanner();
    document.getElementById('tabID').classList.add('active'); document.getElementById('tabQR').classList.remove('active');
    document.getElementById('idInputWrap').style.display = 'block';
    document.getElementById('ticketInput').value = ticket;
    setTimeout(() => enterVenue(), 800);
}

/* ── Venue Entry ── */
async function enterVenue() {
    const input = document.getElementById('ticketInput'), btn = document.getElementById('enterBtn');
    const ticket = input.value.toUpperCase().trim();
    const ticketRegex = /^[A-Z0-9]{2,3}-[A-Z]{2,5}-[A-Z0-9]{2,5}$/;
    if (!ticketRegex.test(ticket)) { showError('Format Mismatch', 'Ticket ID must follow: [TYPE]-[VENUE]-[SEAT]. Try IPL-AMD-G05', false); return; }
    const parts = ticket.split('-');
    const prefix = parts[0], venueCode = parts[1], section = parts[2] || 'G05';
    btn.disabled = true; btn.innerText = 'AUTHENTICATING...';
    if (venueCode === 'MARS') { showError('Node Rejected', 'Virtual spoof attempt detected.', false); resetBtn(btn); return; }
    const knownVenue = VENUE_REGISTRY[venueCode];
    lastAttemptedConfig = { prefix, venueCode, section, name: knownVenue ? knownVenue.name : venueCode };
    if (knownVenue && userCoords) {
        const dist = getDistance(userCoords.lat, userCoords.lon, knownVenue.lat, knownVenue.lon);
        if (dist > 150) { showError('Location Mismatch', `Your location doesn't appear near ${knownVenue.name}. Use Demo Override to continue in demo mode.`, true); resetBtn(btn); return; }
    }
    proceedAnyway();
}

