function parseSection(s, venue) {
    if (!s) return { gate: venue?.gate || 'Gate 7', concession: 'North Food Court', walk: '~2 min (via Corridor B)' };
    const alpha = s.match(/[A-Z]/)?.[0] || 'G';
    const num = parseInt(s.match(/\d+/)?.[0] || '4', 10);
    const gateMap = { A: 1, B: 2, C: 3, D: 4, E: 5, F: 6, G: 7, H: 8 };
    const gateNum = gateMap[alpha] || num || 4;
    const concessions = ['North Food Court', 'South Concourse Bar', 'East Stand Kiosk', 'West Wing Bar', 'VIP Lounge Counter'];
    const concession = concessions[gateNum % concessions.length];
    const walk = `~${1 + (gateNum % 3)} min (via Corridor ${String.fromCharCode(65 + (gateNum % 4))})`;
    return { gate: venue?.gate || `Gate ${gateNum}`, concession, walk };
}

